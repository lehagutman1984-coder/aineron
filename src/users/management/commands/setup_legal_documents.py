class Command(BaseCommand):
    help = 'Заполняет пользовательское соглашение и политику конфиденциальности для aineron.ru'

    def add_arguments(self, parser):
        parser.add_argument(
            '--force', action='store_true',
            help='Перезаписать документы, даже если они уже заполнены (например, через админку)',
        )

    def handle(self, *args, **options):
        force = options['force']
        docs = [
            ('terms', TERMS_TITLE, TERMS_HTML),
            ('privacy', PRIVACY_TITLE, PRIVACY_HTML),
        ]
        for doc_type, title, content in docs:
            obj, created = LegalDocument.objects.get_or_create(
                document_type=doc_type,
                defaults={'title': title, 'content': content.strip()},
            )
            if created:
                self.stdout.write(self.style.SUCCESS(f'{obj.get_document_type_display()}: создан'))
                continue

            is_empty = not obj.content.strip() or obj.content.strip() == PLACEHOLDER
            if force or is_empty:
                obj.title = title
                obj.content = content.strip()
                obj.save()
                self.stdout.write(self.style.SUCCESS(f'{obj.get_document_type_display()}: обновлён'))
            else:
                self.stdout.write(
                    f'{obj.get_document_type_display()}: уже заполнен, пропущен (перезапись: --force)'
                )
